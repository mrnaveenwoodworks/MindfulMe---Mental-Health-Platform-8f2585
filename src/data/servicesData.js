// Utility function to validate service data
export const validateServiceData = (service) => {
  if (!service || typeof service !== "object") return false;
  
  const requiredFields = ["id", "title", "description", "iconType", "category", "features", "ctaLink"];
  const isValid = requiredFields.every(field => service[field] !== null && service[field] !== undefined);
  
  // Ensure image path is a string
  if (service.image === null || service.image === undefined) {
    service.image = `/images/services/${service.id || "default"}.jpg`;
  }
  
  return isValid;
};

// Utility function to get service by ID
export const getServiceById = (id) => {
  if (!id) return null;
  const service = servicesData.find(service => service.id === id) || null;
  return service && validateServiceData(service) ? service : null;
};

// Utility function to get services by category
export const getServicesByCategory = (category) => {
  if (!category) return [];
  
  const filteredServices = category === "all" 
    ? servicesData 
    : servicesData.filter(service => service && service.category === category);
    
  return filteredServices.filter(validateServiceData);
};

// Get featured services
export const getFeaturedServices = (limit = 3) => {
  const validServices = servicesData.filter(validateServiceData);
  // First get highlighted services
  const highlighted = validServices.filter(service => service.highlighted);
  // Then add other services until limit is reached
  const remaining = validServices.filter(service => !service.highlighted);
  
  return [...highlighted, ...remaining].slice(0, limit);
};